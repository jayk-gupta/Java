package Basics;

public class Primitives {
    public static void main(String[] args) {
//        Integer
        int num = 34433;
//        char
        char c = 'r';
//        float: Add a F
//        By  default all the decimal values are double
        float marks = 47.545F;
//        double
        double largeDecimalNum = 567891253.57755;
//        long
        long largerInteger = 9027809L;
//        boolean
        boolean ans = true;

// Note : There are also classes available for the primitive dataTypes


//        String: It is not primitive
        String name = "jay";
//       a is identifier: name of variable,class,packages,functions etc; literals right side data value
        int a = 10;
    }
}
